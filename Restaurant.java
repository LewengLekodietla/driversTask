package CompulsoryTask2;

class Restaurant {

	//Object Attributes
	String restaurantName;
	String restaurantLocation;
	String restaurantContactNumber;
	String orderNumber;

	//The 'Restaurant' class constructor
	public Restaurant(String restaurantName, String restaurantLocation, String restaurantContactNumber, String orderNumber) {

		this.restaurantName = restaurantName;
		this.restaurantLocation = restaurantLocation;
		this.restaurantContactNumber = restaurantContactNumber;
		this.orderNumber = orderNumber;
	}

}
